package api_model;

public class Flavors {
	
	private Flavor flavor;
	private int potency;

	public Flavor getFlavor() {
		return flavor;
	}

	public void setFlavor(Flavor flavor) {
		this.flavor = flavor;
	}
	
	public int getPotency() {
		return potency;
	}
	
	public void setPotency(int potency) {
		this.potency = potency;
	}

}
